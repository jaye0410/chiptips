			<h3 class="heading">
			スター・ウォーズの歴史
			</h3>

			<h4>■ スター・ウォーズの起源</h4>
			<p>スター・ウォーズの歴史がどこから始まったのか、興味はありませんか？</p>
			<p>いろいろな捉え方・考え方があるとは思いますが、私の個人的な見解・価値観からお話すると、
			スター・ウォーズの歴史を研究するならば、「カノンとレジェンズ両方の観点を総合的に見る」方がより楽しめると思っています。</p>

			<p>それは主に次の理由からです。</p>
			<ul>
				<li>カノンもレジェンズからの影響を多大に受けており、常に意識しながら拡大を続けている</li>
				<li>カノンとは矛盾したタイムラインをパラレルワールドとして見るのも面白い</li>
				<li>レジェンズからカノン入りしたキャラクターの背景を事前に知ることができ、相互の違いを比較する楽しみ方もできる</li>
			</ul>

			<p>私が知るカノンおよびレジェンズの作品において、スター・ウォーズの起源、あるいはスター・ウォーズ最古の歴史と言えるものは、
				『Dawn of the Jedi』です。
			</p>
		</section>

		<section class="content">
			<h3 class="heading">
			Dawn of the Jedi
			</h3>

			<h4>■ 『Dawn of the Jedi』とは</h4>
			<p>
				『Dawn of the Jedi』は、スター・ウォーズの世界における最も古い時代を描くコミックシリーズです。<br>
				その物語で扱われる歴史の出発点は、およそ38,000 BBY (ヤヴィンの戦いの38,000年前) 以前まで遡ります。
				(<a href="#notes1" class="explicitly">注釈 ※1</a>)</p>
			<p>
				2024/9/25現在、残念ながら和訳はされておらず、私が所持しているのはコミックス第一巻と第二巻のみです。<br>
				そこで、可能な限り多種多様な文献を漁って情報収集しました。<br>
				『Dawn of the Jedi』の紹介は、不定期ですがブログ記事として投稿する予定でいます。
			</p>
			<p>『Dawn of the Jedi』は、2012年から2014年にかけて、全15巻がダークホースコミックスから刊行されました。</p>
			<p>5巻ずつストーリー・アークが分かれており、3部構成となっています。</p>
			<ul>
				<li>1～5巻：Force Storm (フォースの嵐)</li>
				<li>6～10巻：Prisoner of Bogan (ボガンの囚人)</li>
				<li>11～15巻：Force War (フォース戦争)</li>
			</ul>
			<p>『Dawn of the Jedi』では、ジェダイとジェダイ・オーダーの起源、シスの起源、フォースの起源について言及されており、
				非正史と切り捨ててしまうのはあまりに惜しく、スター・ウォーズの歴史を紐解く上で極めて重要な作品です。<br>
			</p>

