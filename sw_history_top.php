			<h4>■ スローン三部作とは</h4>
			<p>『Heir to the Empire』(邦題：帝国の後継者)、『Dark Force Rising』(邦題：暗黒の艦隊)、『The Last Command』(邦題：最後の指令)
				の三作品から成るレジェンズ小説を指します。
			<br>
			レジェンズ小説は膨大な数があり、私ももちろんすべてを読んでいるわけではありません。<br>
			しかしながら、読んだ作品の中でもスローン三部作の完成度、面白さは傑出していると断言できます。
			</p>
			<p>
			この作品はカノンにも多大な影響を今もなお与え続けており、SWGoHとも深いつながりがあります。<br>
			スローン大提督が正史入りを果たしたのは、ご存じの方も多いかもしれませんが、アニメシリーズ『反乱者たち』です。<br>
			『反乱者たち』にはスローン大提督だけでなく、従者のルクもしっかり登場しており、なかなかの活躍を見せています。
			</p>
			<p>さらに、SWGoHとの繋がりで言えば、マラ・ジェイドもスローン三部作の登場キャラクターです。<br>
